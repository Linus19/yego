                <h5 class="mt-4">What is Capital Gains Tax?</h5>
                <p>Capital Gains Tax (CGT) is a tax levied on the transfer of property situated in Kenya acquired on or
                    before January 2015. The rate of tax is 5% of the gain and is paid by the seller. CGT is a final
                    tax, so no further taxes are due once it is paid.</p>

                <h5 class="mt-4">Types of Capital Gains Tax</h5>
                <ul class="list-unstyled">
                    <li><i class="fas fa-check text-primary"></i> CGT 1 for land and buildings</li>
                    <li><i class="fas fa-check text-primary"></i> CGT 2 for shares</li>
                    <li><i class="fas fa-check text-primary"></i> CGT 3 for exemptions</li>
                </ul>

                <p>Property may be transferred through various means, such as gifting, inheritance, and sales. However,
                    not all transfers are subject to CGT. Below are key terms used in calculating CGT:</p>

                <h5 class="mt-4">Key Terms in Capital Gains Tax</h5>
                <ul class="list-unstyled">
                    <li><i class="fas fa-check text-primary"></i> <strong>Net Transfer Value:</strong> The transfer
                        value minus incidental expenses.</li>
                    <li><i class="fas fa-check text-primary"></i> <strong>Adjusted Cost of Property:</strong> The
                        acquisition cost plus any costs incurred to enhance or maintain the property.</li>
                    <li><i class="fas fa-check text-primary"></i> <strong>Capital Gain or Loss:</strong> The difference
                        between the net transfer value and the adjusted cost.</li>
                </ul>

                <h5 class="mt-4">Exemptions from Capital Gains Tax</h5>
                <p>Certain property transactions are exempt from CGT, including:</p>
                <ul class="list-unstyled">
